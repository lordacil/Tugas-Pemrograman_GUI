# var nomor dengan list jangkauan < 5
nomor = list(range(5))
print(nomor)
# jika fungsi range dipanggil menggunakan 2 argumen,
#maka akan mengembalikan jangkauan nilai dari angka pertama sampai dengan angka kedua.
angka = list(range(2,8))
print(angka)
# fungsi range juga dapat memiliki argumen ketiga.
# fungsinya adalah sebagai interval pada angka pertama dan angka kedua.
angka1 = list(range(1,10,2))
print(angka1)
